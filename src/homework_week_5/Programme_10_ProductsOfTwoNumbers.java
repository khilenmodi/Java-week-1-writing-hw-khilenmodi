package homework_week_5;
/**
 * Write a class of java program that takes two numbers as input and display the
 * product of two numbers.
 * Test Data:
 * Input first number : 25
 * Input second number : 5
 * Expected output : 25 * 5 = 125
 */

public class Programme_10_ProductsOfTwoNumbers {
    public static void main(String[] args) {
        int firstNumber = 25; //declaration of first number
        int secondNumber = 5; // declaration of second number
        int produced = firstNumber * secondNumber;
        //Expected Output
        System.out.println(firstNumber + " X " + secondNumber + " = " + produced);


    }

}

